package models;

public class Offer {
	private int id;
	private String offerType;
	
	
	public int getId() {
		return id;
	}
	public String getOfferType() {
		return offerType;
	}
	public void setId(int id) {
		this.id = id;
	}
	public void setOfferType(String offerType) {
		this.offerType = offerType;
	}
}