package models;

public class City {
	public int cityId;
	public String cityTitle;
	
	public int getCityId() {
		return cityId;
	}
	public String getCityTitle() {
		return cityTitle;
	}
	public void setCityId(int cityId) {
		this.cityId = cityId;
	}
	public void setCityTitle(String cityTitle) {
		this.cityTitle = cityTitle;
	}
}